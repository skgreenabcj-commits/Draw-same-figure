/**
 * 2つの線分 AB と CD が「厳密に内部で交差する」か判定する。
 *
 * 「厳密内部交差」の定義:
 *   - 交点が両線分の端点ではなく、両方の線分の内側に存在する
 *   - 端点が一致する場合は交差とみなさない
 *   - 一方の端点が他方の線分上にある場合も交差とみなさない
 *
 * アルゴリズム: 外積（クロス積）の符号による方向判定
 *   d1, d2 → 線分CD に対して点A, Bがそれぞれどちら側にあるか
 *   d3, d4 → 線分AB に対して点C, Dがそれぞれどちら側にあるか
 *   両ペアの符号が逆（互いに反対側）のときのみ厳密内部交差と判定する
 *
 * @param {number} ax - 線分ABの始点X座標
 * @param {number} ay - 線分ABの始点Y座標
 * @param {number} bx - 線分ABの終点X座標
 * @param {number} by - 線分ABの終点Y座標
 * @param {number} cx - 線分CDの始点X座標
 * @param {number} cy - 線分CDの始点Y座標
 * @param {number} dx - 線分CDの終点X座標
 * @param {number} dy - 線分CDの終点Y座標
 * @returns {boolean} 厳密内部交差する場合 true、それ以外は false
 */
function _cross(ax, ay, bx, by, cx, cy, dx, dy) {
  // ── 端点一致チェック ──────────────────────────────────────────────
  // 始点A が C または D と一致 → 交差なし
  if ((ax === cx && ay === cy) || (ax === dx && ay === dy)) return false;
  // 終点B が C または D と一致 → 交差なし
  if ((bx === cx && by === cy) || (bx === dx && by === dy)) return false;

  // ── 外積値の計算 ──────────────────────────────────────────────────
  // d1: ベクトルCDに対する点Aの外積（点AがCDの左右どちらにあるか）
  const d1 = (dx - cx) * (ay - cy) - (dy - cy) * (ax - cx);
  // d2: ベクトルCDに対する点Bの外積（点BがCDの左右どちらにあるか）
  const d2 = (dx - cx) * (by - cy) - (dy - cy) * (bx - cx);
  // d3: ベクトルABに対する点Cの外積（点CがABの左右どちらにあるか）
  const d3 = (bx - ax) * (cy - ay) - (by - ay) * (cx - ax);
  // d4: ベクトルABに対する点Dの外積（点DがABの左右どちらにあるか）
  const d4 = (bx - ax) * (dy - ay) - (by - ay) * (dx - ax);

  // ── 交差判定 ──────────────────────────────────────────────────────
  // d1 と d2 の符号が異なる → A と B が CD を挟んで反対側にある
  // d3 と d4 の符号が異なる → C と D が AB を挟んで反対側にある
  // 両条件が成立した場合のみ厳密内部交差と判定
  return (
    ((d1 > 0 && d2 < 0) || (d1 < 0 && d2 > 0)) &&
    ((d3 > 0 && d4 < 0) || (d3 < 0 && d4 > 0))
  );
}

/**
 * 線分配列内のすべてのペアについて厳密内部交差の数を数える。
 *
 * 計算量: O(n²) — 線分数 n が小さい（4〜5本）ため十分高速
 *
 * @param {Array<{x1:number, y1:number, x2:number, y2:number}>} lines
 *   線分オブジェクトの配列
 * @returns {number} 厳密内部交差のペア数
 */
function _countCross(lines) {
  let n = 0;
  // すべての組み合わせ (i, j) を二重ループで検査（重複なし）
  for (let i = 0; i < lines.length; i++) {
    for (let j = i + 1; j < lines.length; j++) {
      if (_cross(
        lines[i].x1, lines[i].y1, lines[i].x2, lines[i].y2,
        lines[j].x1, lines[j].y1, lines[j].x2, lines[j].y2
      )) {
        n++;
      }
    }
  }
  return n;
}

