/* ── 公開API ─────────────────────────────────────────────────────────
 * 指定レベルの問題バンクからランダムに5問選んで返す。
 * Fisher-Yates シャッフルで毎回異なる順序を保証する。
 *
 * @param {number} level - レベル番号 (0–3)
 * @returns {Array} シャッフルされた5問の配列
 * ─────────────────────────────────────────────────────────────────── */
function getProblems(level){
  // 指定レベルが存在しない場合はLv1にフォールバック
  const pool = (PROBLEM_BANK[level] || PROBLEM_BANK[1]).slice();
  // Fisher-Yates シャッフル: 末尾から前方へランダム入れ替え
  for(let i=pool.length-1;i>0;i--){
    const j=Math.floor(Math.random()*(i+1));
    [pool[i],pool[j]]=[pool[j],pool[i]];
  }
  // 先頭5問を返す（バンクには10問あるため毎回異なる組み合わせになる）
  return pool.slice(0,5);
}

