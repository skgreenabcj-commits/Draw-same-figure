/**
 * 指定レベルの問題をシャッフルして5問返す。
 * hintLines は HINT_COUNTS を参照して先頭N本の線分オブジェクト配列として付与する。
 *
 * @param {number} level
 * @returns {Array}
 */
function getProblems(level) {
  const pool = (PROBLEM_BANK[level] || PROBLEM_BANK[1]).slice();

  // Fisher-Yates シャッフル
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }

  const hintCount = HINT_COUNTS[level] ?? 0;

  return pool.slice(0, 5).map(p => ({
    ...p,
    hintLines: p.lines.slice(0, hintCount)
  }));
}
