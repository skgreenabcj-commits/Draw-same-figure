/* ── 公開API: getProblems() ──────────────────────────────────────────
 *
 * 【Bug #3 / #4 修正箇所】
 * 旧版では hintLines が付与されず、canvas.js や app.js に渡すオブジェクト
 * に hintLines フィールドが存在しなかった。
 *
 * 修正内容:
 *   1. HINT_COUNTS テーブルからレベル別のヒント本数を取得する
 *   2. problem.lines の先頭 hintCount 本を hintLines として切り出す
 *      → 実際の線分オブジェクト（{x1,y1,x2,y2}）の配列になる
 *      → canvas.js の drawAnswer() が直接 drawLine() に渡せる形式
 *      → app.js の judgeAnswer() が normalize() できる形式
 *   3. hintLines が 0 本（Lv2, Lv3）の場合は空配列を返す
 *
 * @param {number} level - レベル番号 (0–3)
 * @returns {Array<Object>} シャッフルされた5問。各問題に hintLines を含む。
 * ──────────────────────────────────────────────────────────────────── */
function getProblems(level) {
  // 指定レベルが存在しない場合は Lv1 にフォールバック
  const pool = (PROBLEM_BANK[level] || PROBLEM_BANK[1]).slice();

  // Fisher-Yates シャッフル: 末尾から前方へ順にランダムな位置と入れ替え
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }

  // ヒント本数を HINT_COUNTS から取得（未定義レベルは 0 扱い）
  const hintCount = HINT_COUNTS[level] ?? 0;

  // 先頭5問を返す。hintLines を線分オブジェクトの配列として付与する。
  return pool.slice(0, 5).map(p => ({
    ...p,                                       // level, grid, lines をそのままコピー
    hintLines: p.lines.slice(0, hintCount)      // ← Bug #3 修正: 線分オブジェクトの配列
    //
    // 【なぜ slice(0, hintCount) か】
    // 旧版では hintLines が存在しないか、gemini.js から [0,1,2] のような
    // 数値インデックス配列として渡されていた。
    // canvas.js の drawLine(ctx, g, line, ...) は line.x1, line.y1 等を
    // 参照するため、数値が渡されると undefined になりヒントが描画されない。
    // judgeAnswer() の normalize(l) も l.x1 等を参照するため同様に破綻する。
    // 修正後は {x1, y1, x2, y2} オブジェクトをそのまま slice するため
    // 型が一致し、描画・判定・残り本数表示がすべて正しく動作する。
  }));
}

/* ── 起動時自己チェック（コンソール出力のみ・ノンブロッキング） ──────
 *
 * DOMContentLoaded を待たず即時実行する IIFE。
 * 全問題の交差数をカウントし、レベル別制約に違反がないか確認する。
 * エラーがあれば console.error で報告するが、ゲームの動作は止めない。
 * ──────────────────────────────────────────────────────────────────── */
(function _selfCheck() {
  // レベル別の交差数許容範囲 [最小値, 最大値]
  const LIMITS = { 0: [0, 2], 1: [0, 3], 2: [2, 5], 3: [0, 8] };
  let ok = true;

  for (const [lvStr, problems] of Object.entries(PROBLEM_BANK)) {
    const lv       = Number(lvStr);
    const [lo, hi] = LIMITS[lv] || [0, 99]; // 未定義レベルは制限なし

    problems.forEach((p, i) => {
      const n = _countCross(p.lines); // 実際の交差数を計算
      if (n < lo || n > hi) {
        // 制約違反: レベル・問題番号・実交差数・許容範囲をすべて報告
        console.error(
          `[problems.js] FAIL Lv${lv} #${i + 1}: ` +
          `交差数=${n}, 許容範囲=${lo}–${hi}`
        );
        ok = false;
      }
    });
  }

  if (ok) {
    console.log('[problems.js] ✅ 全問題の交差数チェック合格。');
  }
})();
