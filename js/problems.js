/* ── 交差判定ヘルパー関数（gemini.js の _cross と完全一致） ────────── */

/*
 * 2つの線分 AB と CD が「厳密に内部で交差する」か判定する。
 * - 端点が一致する場合は交差とみなさない
 * - d1〜d4 は外積の符号で各端点の位置関係を判定する
 *
 * @param {number} ax,ay - 線分ABの始点
 * @param {number} bx,by - 線分ABの終点
 * @param {number} cx,cy - 線分CDの始点
 * @param {number} dx,dy - 線分CDの終点
 * @returns {boolean} 厳密内部交差する場合 true
 */
function _cross(ax,ay,bx,by,cx,cy,dx,dy){
  // 端点が一致する場合は交差なしとして除外
  if((ax===cx&&ay===cy)||(ax===dx&&ay===dy)) return false;
  if((bx===cx&&by===cy)||(bx===dx&&by===dy)) return false;
  // 外積値を計算（符号で端点の位置関係を判断）
  const d1=(dx-cx)*(ay-cy)-(dy-cy)*(ax-cx);
  const d2=(dx-cx)*(by-cy)-(dy-cy)*(bx-cx);
  const d3=(bx-ax)*(cy-ay)-(by-ay)*(cx-ax);
  const d4=(bx-ax)*(dy-ay)-(by-ay)*(dx-ax);
  // 両線分が互いの反対側にある場合のみ交差と判定
  return ((d1>0&&d2<0)||(d1<0&&d2>0))&&((d3>0&&d4<0)||(d3<0&&d4>0));
}

/*
 * 線分配列内のすべてのペアについて交差数を数える。
 *
 * @param {Array} lines - {x1,y1,x2,y2} 形式の線分オブジェクト配列
 * @returns {number} 厳密内部交差のペア数
 */
function _countCross(lines){
  let n=0;
  // すべての線分ペア (i, j) を二重ループで検査
  for(let i=0;i<lines.length;i++)
    for(let j=i+1;j<lines.length;j++)
      if(_cross(lines[i].x1,lines[i].y1,lines[i].x2,lines[i].y2,
                lines[j].x1,lines[j].y1,lines[j].x2,lines[j].y2)) n++;
  return n;
}

